class UnaryOperator {
public static void main(String ar []) {
int opr=0;
opr+=3;
System.out.println(opr++); //3 3
System.out.println(opr); //4 4
System.out.println(++opr); //4 5
System.out.println(opr); //4 5
System.out.println(opr--); //3 5
System.out.println(opr); //2 4
System.out.println(--opr); //2 3
System.out.println(opr); //2 3
}
}